#!/usr/bin/env node
/**
 * Generate TS interfaces for the frontend from `backend/prisma/schema.prisma`.
 *
 * Cycle 42 scaffold: parses a small handful of Prisma models and emits a
 * matching `types/prisma.ts` file so the frontend stops re-declaring the
 * same shapes by hand.
 *
 * Scope (this pass):
 *  - 5 models only (User, Chat, Message, File, AgentTask).
 *  - Sensitive fields are excluded: password, googleId, stripeCustomerId,
 *    stripeSubscriptionId, refreshToken, resetToken, microsoftId, twoFactorSecret.
 *  - Relations (other model references) are dropped — keep this generator
 *    flat. The frontend can reach for individual envelope types when it
 *    needs the joined shape.
 *
 * Usage:
 *   node scripts/generate-prisma-types-for-frontend.js
 *
 * The script is idempotent — re-running overwrites `types/prisma.ts`.
 */
'use strict';

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const SCHEMA_PATH = path.join(REPO_ROOT, 'backend', 'prisma', 'schema.prisma');
const OUTPUT_PATH = path.join(REPO_ROOT, 'types', 'prisma.ts');

const ALLOWED_MODELS = new Set(['User', 'Chat', 'Message', 'File', 'AgentTask']);

const SENSITIVE_FIELDS = new Set([
  'password',
  'passwordHash',
  'googleId',
  'microsoftId',
  'githubId',
  'stripeCustomerId',
  'stripeSubscriptionId',
  'stripeAccountId',
  'refreshToken',
  'resetToken',
  'verificationToken',
  'twoFactorSecret',
  'apiKey',
  'apiSecret',
]);

// Prisma → TS scalar map. Unknown types fall through to `unknown` so the
// generated file stays compilable even when new Prisma scalars appear.
const SCALAR_MAP = {
  String: 'string',
  Int: 'number',
  BigInt: 'bigint',
  Float: 'number',
  Decimal: 'number',
  Boolean: 'boolean',
  DateTime: 'string', // serialised over JSON
  Json: 'unknown',
  Bytes: 'string',
};

function parseSchema(source) {
  const models = [];
  const modelRegex = /model\s+(\w+)\s*{([^}]*)}/gms;
  let match;
  while ((match = modelRegex.exec(source)) !== null) {
    const [, name, body] = match;
    if (!ALLOWED_MODELS.has(name)) continue;

    const fields = [];
    for (const rawLine of body.split('\n')) {
      const line = rawLine.trim();
      if (!line || line.startsWith('//') || line.startsWith('@@')) continue;

      // Field syntax: `name Type[? | []] [@attrs...]`
      const fieldMatch = line.match(/^(\w+)\s+(\S+)(.*)$/);
      if (!fieldMatch) continue;
      const [, fieldName, rawType] = fieldMatch;

      if (SENSITIVE_FIELDS.has(fieldName)) continue;

      // Skip relation fields — they reference other models. Detected by
      // having `@relation` in the attrs OR by the type being an
      // uppercase model name (not a Prisma scalar).
      const isRelation = /\s@relation/.test(line);
      const isOptional = rawType.endsWith('?');
      const isList = rawType.endsWith('[]');
      const baseType = rawType.replace(/[?\[\]]/g, '');

      const tsScalar = SCALAR_MAP[baseType];
      // Drop relation fields (no scalar mapping) — keep generator flat.
      if (!tsScalar) {
        // Uppercase non-scalar type = either another model (relation) or
        // an enum. Treat capitalised list types and any field explicitly
        // marked `@relation` as relations and drop. Enums (uppercase
        // singular without `[]`) keep their name as a string union
        // placeholder so the front-end can refine later.
        const looksLikeModelOrEnum = /^[A-Z]/.test(baseType);
        if (isRelation || (looksLikeModelOrEnum && isList)) continue;
        if (looksLikeModelOrEnum && !isList) {
          // Likely an enum — emit as string (Prisma enums serialise as
          // their value string over JSON).
          fields.push({
            name: fieldName,
            tsType: 'string',
            optional: isOptional,
            list: false,
          });
          continue;
        }
        // Unknown — fall through as `unknown`.
        fields.push({
          name: fieldName,
          tsType: 'unknown',
          optional: isOptional,
          list: isList,
        });
        continue;
      }

      fields.push({
        name: fieldName,
        tsType: tsScalar,
        optional: isOptional,
        list: isList,
      });
    }

    models.push({ name, fields });
  }
  return models;
}

function emit(models) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-prisma-types-for-frontend.js.',
    '// DO NOT EDIT BY HAND. Re-run the generator to regenerate.',
    '// Source: backend/prisma/schema.prisma',
    '//',
    '// Scope (cycle 42 scaffold): 5 models, sensitive fields stripped,',
    '// relations dropped. Extend ALLOWED_MODELS in the generator to add more.',
    '',
  ];

  for (const model of models) {
    lines.push(`export interface ${model.name} {`);
    for (const field of model.fields) {
      const optionalMarker = field.optional ? '?' : '';
      const tsType = field.list ? `${field.tsType}[]` : field.tsType;
      lines.push(`  ${field.name}${optionalMarker}: ${tsType};`);
    }
    lines.push('}');
    lines.push('');
  }

  return lines.join('\n');
}

function main() {
  if (!fs.existsSync(SCHEMA_PATH)) {
    console.error(`schema not found at ${SCHEMA_PATH}`);
    process.exit(1);
  }
  const source = fs.readFileSync(SCHEMA_PATH, 'utf8');
  const models = parseSchema(source);
  if (models.length === 0) {
    console.error('No matching models found. Check ALLOWED_MODELS.');
    process.exit(1);
  }
  const output = emit(models);
  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, output);
  console.log(`Wrote ${models.length} models to ${path.relative(REPO_ROOT, OUTPUT_PATH)}`);
}

if (require.main === module) main();

module.exports = { parseSchema, emit, ALLOWED_MODELS, SENSITIVE_FIELDS };
