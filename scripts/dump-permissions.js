#!/usr/bin/env node
// ──────────────────────────────────────────────────────────────
// siraGPT — RBAC permission matrix dump (F1 PR5)
// ──────────────────────────────────────────────────────────────
// Parses the role / permission seed migration and emits a
// stable Markdown matrix to stdout, or compares it against the
// committed snapshot at `docs/architecture/rbac-matrix.md` when
// invoked with `--check`. Drives a CI guard so accidental
// role_permissions changes can't land without updating the
// human-readable matrix.
//
// Source of truth: the seed inside
//   backend/prisma/migrations/20260524010000_add_role_permission_tables/migration.sql
//
// Usage:
//   node scripts/dump-permissions.js              # print matrix
//   node scripts/dump-permissions.js --check      # compare against snapshot
//   node scripts/dump-permissions.js --update     # rewrite the snapshot
// ──────────────────────────────────────────────────────────────

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const MIGRATION = path.join(
  ROOT,
  'backend',
  'prisma',
  'migrations',
  '20260524010000_add_role_permission_tables',
  'migration.sql',
);
const SNAPSHOT = path.join(ROOT, 'docs', 'architecture', 'rbac-matrix.md');

const ROLES = ['SUPERADMIN', 'ORG_OWNER', 'ORG_ADMIN', 'ORG_MEMBER', 'ORG_VIEWER', 'USER'];

function readSql() {
  return fs.readFileSync(MIGRATION, 'utf8');
}

function extractAllPermissionCodes(sql) {
  // The permission seed block is `INSERT INTO "permissions" ... VALUES
  //   ('perm_xxx', 'resource.action', '...', TRUE), ...`
  // Pull every code from the second column of each tuple in the
  // permissions seed block.
  const block = sql.match(/INSERT INTO "permissions"[\s\S]+?ON CONFLICT/);
  if (!block) return [];
  const codes = [];
  const re = /\(\s*'[^']+'\s*,\s*'([^']+)'\s*,/g;
  let m;
  while ((m = re.exec(block[0])) !== null) {
    codes.push(m[1]);
  }
  return codes.sort();
}

function extractRolePermissions(sql, roleCode) {
  // For SUPERADMIN the seed uses a CROSS JOIN with all permissions.
  if (roleCode === 'SUPERADMIN') {
    if (/CROSS JOIN "permissions" p[\s\S]+?r\."code" = 'SUPERADMIN'/.test(sql)) {
      return extractAllPermissionCodes(sql);
    }
    return [];
  }
  // Anchor the match using the role's deterministic id prefix
  // (`rp_<role>_…`) that appears in the SELECT — uniquely identifying
  // this role's INSERT block and avoiding cross-block matches that a
  // naive `INSERT INTO … WHERE r.code='X'` regex would produce.
  const idPrefix = 'rp_' + roleCode.toLowerCase() + '_';
  const re = new RegExp(
    `'${idPrefix}'[\\s\\S]+?p\\."code" IN \\(([^)]+)\\)`,
  );
  const m = sql.match(re);
  if (!m) return [];
  const items = m[1].match(/'([^']+)'/g) || [];
  return items.map((s) => s.slice(1, -1)).sort();
}

function buildMatrix() {
  const sql = readSql();
  const allPermissions = extractAllPermissionCodes(sql);
  const roleSets = Object.fromEntries(
    ROLES.map((r) => [r, new Set(extractRolePermissions(sql, r))]),
  );

  const lines = [];
  lines.push('# RBAC Permission Matrix (auto-generated)');
  lines.push('');
  lines.push(
    '<!-- Generated by `node scripts/dump-permissions.js`. Do NOT edit by hand.',
  );
  lines.push(
    '     Source: backend/prisma/migrations/20260524010000_add_role_permission_tables/migration.sql.',
  );
  lines.push('     Regenerate after every role_permissions change with `--update`. -->');
  lines.push('');
  lines.push(`Roles: ${ROLES.length} system roles, isSystem=true.`);
  lines.push(`Permissions: ${allPermissions.length} canonical \`resource.action\` codes.`);
  lines.push('');

  const header = ['Permission', ...ROLES];
  const align = ['---', ...ROLES.map(() => ':---:')];
  lines.push(`| ${header.join(' | ')} |`);
  lines.push(`| ${align.join(' | ')} |`);
  for (const perm of allPermissions) {
    const row = [
      `\`${perm}\``,
      ...ROLES.map((r) => (roleSets[r].has(perm) ? '✓' : '')),
    ];
    lines.push(`| ${row.join(' | ')} |`);
  }
  lines.push('');
  // Per-role summary counters for quick scanning.
  lines.push('## Per-role permission count');
  lines.push('');
  lines.push('| Role | Permissions |');
  lines.push('| --- | ---: |');
  for (const role of ROLES) {
    lines.push(`| \`${role}\` | ${roleSets[role].size} |`);
  }
  lines.push('');

  return lines.join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const flag = args[0] || '';
  const matrix = buildMatrix();

  if (flag === '--check') {
    if (!fs.existsSync(SNAPSHOT)) {
      console.error(`[dump-permissions] missing snapshot at ${SNAPSHOT}`);
      console.error('  run: node scripts/dump-permissions.js --update');
      process.exit(2);
    }
    const current = fs.readFileSync(SNAPSHOT, 'utf8');
    if (current.trimEnd() === matrix.trimEnd()) {
      console.log('[dump-permissions] OK — snapshot matches migration');
      process.exit(0);
    }
    console.error('[dump-permissions] DRIFT DETECTED');
    console.error(`  Snapshot: ${path.relative(ROOT, SNAPSHOT)}`);
    console.error('  Re-run:   node scripts/dump-permissions.js --update');
    process.exit(1);
  }

  if (flag === '--update') {
    fs.mkdirSync(path.dirname(SNAPSHOT), { recursive: true });
    fs.writeFileSync(SNAPSHOT, matrix + '\n', 'utf8');
    console.log(`[dump-permissions] wrote ${path.relative(ROOT, SNAPSHOT)}`);
    process.exit(0);
  }

  process.stdout.write(matrix + '\n');
}

if (require.main === module) {
  try {
    main();
  } catch (err) {
    console.error('[dump-permissions] fatal:', err && err.message ? err.message : err);
    process.exit(2);
  }
}

module.exports = { buildMatrix, extractAllPermissionCodes, extractRolePermissions };
