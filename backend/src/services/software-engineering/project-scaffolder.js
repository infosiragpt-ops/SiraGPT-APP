function substitute(tree, vars) {
  return tree.map(f => ({
    ...f,
    content: f.content.replace(/\{\{PROJECT_NAME\}\}/g, vars.projectName || "app"),
  }));
}

/**
 * Build a scaffold descriptor.
 * @param {object} args
 * @param {string} args.stack      — 'nextjs' | 'fastapi'
 * @param {string} args.projectName
 */
function scaffoldProject({ stack, projectName }) {
  const desc = STACK_DESCRIPTORS[stack];
  if (!desc) {
    return { ok: false, error: `Unknown stack "${stack}". Supported: ${Object.keys(STACK_DESCRIPTORS).join(", ")}` };
  }
  const clean = String(projectName || "app").replace(/[^a-zA-Z0-9._-]/g, "-").slice(0, 80) || "app";
  let rawFiles;
  if (stack === "nextjs") rawFiles = nextjsFiles(clean);
  else if (stack === "fastapi") rawFiles = fastapiFiles(clean);
  else return { ok: false, error: "no emitter for stack" };
  const files = substitute(rawFiles, { projectName: clean });
  return {
    ok: true,
    stack: desc,
    rootName: clean,
    files,
    dependencies: desc.dependencies,
    commands: desc.commands,
    acceptance_tests: desc.acceptance_tests,
  };
}

function listStacks() {
  return Object.values(STACK_DESCRIPTORS).map(s => ({
    id: s.id,
    name: s.name,
    language: s.language,
    commands: s.commands,
  }));
}

