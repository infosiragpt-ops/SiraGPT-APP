/**
 * project-scaffolder — emits a complete file-tree descriptor for a
 * new project so the orchestrator can unpack it to disk atomically.
 *
 * Intentional non-goals:
 *   - NOT an opinionated code generator (LLM fills bodies).
 *   - NOT a pipeline runner (buildRunner + deploymentAgent do that).
 *   - NOT touching fs here — caller decides when to persist.
 *
 * The return shape is purely declarative:
 *   {
 *     ok, stack, rootName, files: [{ path, content, kind }],
 *     dependencies: { prod, dev }, commands: { dev, build, test, lint },
 *     acceptance_tests: [string],
 *   }
 *
 * `content` is either a full literal string or a small template with
 * `{{placeholder}}` markers the caller substitutes before write. We
 * keep every file ≤ ~80 lines: the LLM expands boilerplate further
 * per the user's actual feature set.
 *
 * Supported stacks today: `nextjs` (App Router + TS + Tailwind),
 * `fastapi` (Python 3.11 + pydantic + pytest).
 */

