test('worker and controller use pinned upstream images and explicit local versions', () => {
  for (const source of [workerDockerfile, controllerDockerfile]) {
    assert.match(source, /FROM node:22\.17\.0-bookworm-slim@sha256:[a-f0-9]{64}/);
    assert.doesNotMatch(source, /:latest\b/);
  }
  assert.match(workerDockerfile, /FROM oven\/bun:1\.3\.14@sha256:[a-f0-9]{64}/);
  assert.match(workerDockerfile, /^USER 10001:10001$/m);
  assert.match(controllerDockerfile, /^USER 65532:65532$/m);
  assert.match(compose, /image:\s*siragpt-runsc-controller:0\.1\.0/);
  assert.match(compose, /RUNSC_SANDBOX_WORKER_IMAGE:\s*"\$\{RUNSC_SANDBOX_WORKER_IMAGE:-\}"/);
  assert.match(utils, /inspect\.Image !== config\.image/);
  assert.match(activityStore, /sira\.runsc-sandbox-activity\.v1/);
});

test('real gVisor workflow is path-gated, pinned, and exercises adversarial isolation', () => {
  assert.match(workflow, /runs-on:\s*ubuntu-24\.04/);
  assert.match(workflow, /RUNSC_VERSION:\s*release-\d{8}\.\d+/);
  assert.match(workflow, /RUNSC_SHA512_X86_64:\s*[a-f0-9]{128}/);
  assert.match(workflow, /actions\/checkout@[a-f0-9]{40}/);
  assert.doesNotMatch(workflow, /uses:\s*[^\s]+@(main|master|v\d+)\b/);
  assert.doesNotMatch(workflow, /:latest\b/);
  assert.match(workflow, /--mount type=bind,src=\/var\/run\/docker\.sock,dst=\/var\/run\/docker\.sock,readonly/);
  assert.match(workflow, /WORKER_IMAGE_ID=\$\{worker_image_id\}/);
  assert.match(workflow, /RUNSC_SANDBOX_WORKER_IMAGE="\$\{WORKER_IMAGE_ID\}"/);
  assert.match(workflow, /runtimeArgs == \["--platform=systrap", "--network=sandbox"\]/);
  assert.match(workflow, /--publish 127\.0\.0\.1:4098:4098/);
  assert.match(workflow, /test -z "\$\(docker ps -aq --filter label=com\.siragpt\.runsc-sandbox\.managed=true\)"/);
  for (const sourcePath of [
    'scripts/runsc-sandbox-*.js',
    'scripts/runsc-sandbox-*.mjs',
    'scripts/runsc-sandbox-*.Dockerfile',
    'backend/src/services/codex/sandbox-providers/**',
    'docker-compose.prod.yml',
  ]) {
    const occurrences = workflow.match(new RegExp(sourcePath.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')) || [];
    assert.equal(occurrences.length, 2, `${sourcePath} must gate pull_request and production-main push`);
  }
  assert.match(smoke, /sandbox B read A's workspace/);
  assert.match(smoke, /CONTROL_PLANE_BLOCKED/);
  assert.match(smoke, /NO_DEFAULT_ROUTE/);
  assert.match(smoke, /PID_LIMIT_ENFORCED/);
  assert.match(smoke, /exec_timeout/);
  assert.match(smoke, /memory pressure escaped the cgroup limit/);
  assert.match(smoke, /DELETE.*v1\/sandboxes/);
});

