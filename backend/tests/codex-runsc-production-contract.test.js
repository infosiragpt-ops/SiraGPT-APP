'use strict';

const { test } = require('node:test');
const assert = require('node:assert/strict');
const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '../..');
const compose = fs.readFileSync(path.join(ROOT, 'docker-compose.prod.yml'), 'utf8');
const envExample = fs.readFileSync(path.join(ROOT, '.env.example'), 'utf8');
const workerDockerfile = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-worker.Dockerfile'), 'utf8');
const controllerDockerfile = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-controller.Dockerfile'), 'utf8');
const controller = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-controller.js'), 'utf8');
const dockerApi = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-docker-api.js'), 'utf8');
const utils = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-controller-utils.js'), 'utf8');
const activityStore = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-activity-store.js'), 'utf8');
const workflow = fs.readFileSync(path.join(ROOT, '.github/workflows/runsc-sandbox-provider-smoke.yml'), 'utf8');
const smoke = fs.readFileSync(path.join(ROOT, 'scripts/runsc-sandbox-controller-smoke.mjs'), 'utf8');
const pkg = require('../package.json');

function serviceBlock(name, nextName) {
  const start = compose.indexOf(`  ${name}:`);
  assert.notEqual(start, -1, `missing ${name} service`);
  const end = nextName ? compose.indexOf(`  ${nextName}:`, start + 1) : compose.indexOf('\nvolumes:', start);
  return compose.slice(start, end === -1 ? compose.length : end);
}

test('only the profile-gated controller mounts Docker socket; backend and workers never do', () => {
  const controllerBlock = serviceBlock('runsc-sandbox-controller');
  const backend = serviceBlock('backend', 'frontend');
  const runner = serviceBlock('runner', 'backend');
  assert.match(controllerBlock, /profiles:\s*\["runsc-sandbox"\]/);
  assert.match(controllerBlock, /\/var\/run\/docker\.sock:\/var\/run\/docker\.sock:ro/);
  assert.doesNotMatch(backend, /docker\.sock/);
  assert.doesNotMatch(runner, /docker\.sock/);
  assert.match(controllerBlock, /user:\s*"65532:65532"/);
  assert.match(controllerBlock, /cap_drop:\s*\n\s*- ALL/);
  assert.match(controllerBlock, /no-new-privileges:true/);
  assert.match(controllerBlock, /127\.0\.0\.1:4098:4098/);
  assert.match(controllerBlock, /networks:\s*\n\s*- sandbox_controller/);
  assert.match(controllerBlock, /runsc_controller_state:\/var\/lib\/sira-runsc-controller/);
});

test('runsc provider remains off, non-public, and secretless by default', () => {
  const backend = serviceBlock('backend', 'frontend');
  assert.match(backend, /CODEX_SANDBOX_PROVIDER:\s*\$\{CODEX_SANDBOX_PROVIDER:-shared-runner\}/);
  assert.match(backend, /CODEX_RUNSC_SANDBOX_ENABLED:\s*\$\{CODEX_RUNSC_SANDBOX_ENABLED:-false\}/);
  assert.match(backend, /CODEX_RUNSC_CONTROLLER_URL:\s*\$\{CODEX_RUNSC_CONTROLLER_URL:-http:\/\/runsc-sandbox-controller:4098\}/);
  assert.match(envExample, /CODEX_RUNSC_SANDBOX_ENABLED=false/);
  assert.match(envExample, /CODEX_RUNSC_CONTROLLER_URL=http:\/\/127\.0\.0\.1:4098/);
  assert.match(backend, /CODEX_AGENT_OPEN_TO_ALL:\s*\$\{CODEX_AGENT_OPEN_TO_ALL:-false\}/);
  assert.match(utils, /publicMultiTenant:\s*false,\s*secretRefs:\s*false/);
  assert.doesNotMatch(utils, /DATABASE_URL=/);
});

